public class Exemplofor {

    /*
     * O for é uma estrutura de repetição que permite executar um bloco de código
     * várias vezes. A estrutura do for é composta por três partes: a inicialização
     * da variável de controle, a condição de continuação e o incremento ou
     * decremento da variável de controle.
     */

    public static void main(String[] args) throws Exception {
        for (int i = 0; i < 10; i++) {
            System.out.println("Valor de i: " + i);
        }
    }
}
