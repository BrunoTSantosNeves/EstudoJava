public class loopWhile {

    /*
     * O while é uma estrutura de repetição que permite executar um bloco de código
     * várias vezes. A estrutura do while é composta por uma condição de continuação
     * e um bloco de código.
     */

    public static void main(String[] args) {
        int i = 0;
        while (i < 10) {
            System.out.println("Valor de i: " + i);
            i++;
        }
    }
}